public class Main {
    public static void main(String[] args) throws Exception {

        // Criação do Tabuleiro de Xadrez
        int[][] tabuleiro = new int[8][8];

        for (int i = 0; i < tabuleiro.length; i++) {
            for (int j = 0; j < tabuleiro.length; j++) {
                tabuleiro[i][j] = 0;
            }
        }



    }
}
